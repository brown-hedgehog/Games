package util;

public class Dot {
	private int row;
	private int column;

	public Dot(int row, int column) {
		super();
		this.row = row;
		this.column = column;
	}

	public Dot() {
		super();
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getColumn() {
		return column;
	}

	public void setColumn(int column) {
		this.column = column;
	}

}
